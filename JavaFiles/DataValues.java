public class DataValues{
        public static void main(String[] args) {
        int a = 10 , e = 20 , f = 30;
        double b = 10.5;
        char c = 'A';
        String d = "Hello World !";
        System.out.println("Integer : " + a + " " + e + " " + f);
        System.out.println("Double : " + b);
        System.out.println("Character : " + c);
        System.out.println("String : " + d);
    }
}
